public class Product {

    String modello;
    String marca;
    double prezzo;
    String colore;
    double peso;

    public Product(String modello, String marca, double prezzo, String colore, double peso) {
        this.modello = modello;
        this.marca = marca;
        this.prezzo = prezzo;
        this.colore = colore;
        this.peso = peso;
    }

}